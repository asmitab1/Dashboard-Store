
package com.dashboard.javabean; 

public class DefectResolutionVO {
	
	private String team;
	private String ticketNumber;
	private String issueDescription;
	private String resolution;
	private String mitigatedRiskOpportunity;
	/**
	 * @return the team
	 */
	public String getTeam() {
		return team;
	}
	/**
	 * @param team the team to set
	 */
	public void setTeam(String team) {
		this.team = team;
	}
	/**
	 * @return the ticketNumber
	 */
	public String getTicketNumber() {
		return ticketNumber;
	}
	/**
	 * @param ticketNumber the ticketNumber to set
	 */
	public void setTicketNumber(String ticketNumber) {
		this.ticketNumber = ticketNumber;
	}
	/**
	 * @return the issueDescription
	 */
	public String getIssueDescription() {
		return issueDescription;
	}
	/**
	 * @param issueDescription the issueDescription to set
	 */
	public void setIssueDescription(String issueDescription) {
		this.issueDescription = issueDescription;
	}
	/**
	 * @return the resolution
	 */
	public String getResolution() {
		return resolution;
	}
	/**
	 * @param resolution the resolution to set
	 */
	public void setResolution(String resolution) {
		this.resolution = resolution;
	}
	/**
	 * @return the mitigatedRiskOpportunity
	 */
	public String getMitigatedRiskOpportunity() {
		return mitigatedRiskOpportunity;
	}
	/**
	 * @param mitigatedRiskOpportunity the mitigatedRiskOpportunity to set
	 */
	public void setMitigatedRiskOpportunity(String mitigatedRiskOpportunity) {
		this.mitigatedRiskOpportunity = mitigatedRiskOpportunity;
	} 
	
}
