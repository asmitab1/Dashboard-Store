package com.dashboard.javabean;


public class AssignedDefectCountVO {

	private String name;
	private int urgentCount;
	private int highCount;
	private int mediumCount;
	private int lowCount;
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the urgentCount
	 */
	public int getUrgentCount() {
		return urgentCount;
	}
	/**
	 * @param urgentCount the urgentCount to set
	 */
	public void setUrgentCount(int urgentCount) {
		this.urgentCount = urgentCount;
	}
	/**
	 * @return the highCount
	 */
	public int getHighCount() {
		return highCount;
	}
	/**
	 * @param highCount the highCount to set
	 */
	public void setHighCount(int highCount) {
		this.highCount = highCount;
	}
	/**
	 * @return the mediumCount
	 */
	public int getMediumCount() {
		return mediumCount;
	}
	/**
	 * @param mediumCount the mediumCount to set
	 */
	public void setMediumCount(int mediumCount) {
		this.mediumCount = mediumCount;
	}
	/**
	 * @return the lowCount
	 */
	public int getLowCount() {
		return lowCount;
	}
	/**
	 * @param lowCount the lowCount to set
	 */
	public void setLowCount(int lowCount) {
		this.lowCount = lowCount;
	}
	
}